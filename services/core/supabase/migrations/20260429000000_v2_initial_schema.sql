-- ============================================================================
-- Cebby v2 — initial schema
-- ============================================================================
--
-- Single coherent migration for a fresh Supabase project. Replaces the
-- accreted v1 schema (services/core/supabase/migrations/) with a clean
-- design grounded in:
--
--   • .impeccable.md  — Cebby is a calm, public-utility directory. Not an
--                       organizer toolkit, not RSVP, not a community network.
--                       The whitelist is the moat.
--   • ARCHITECTURE.md — multi-source attribution model.
--
-- Conventions used throughout:
--   • bigint IDs with GENERATED BY DEFAULT AS IDENTITY (lets the migration
--     script preserve v1 numeric ids during cutover).
--   • Every user-content table has created_at + updated_at maintained via
--     trigger. updated_at powers HTTP cache validation at the edge.
--   • Foreign keys are explicit about ON DELETE behavior — CASCADE only for
--     genuinely owned children, SET NULL for soft links.
--   • Status as an enum type, not a boolean. is_hidden conflates several
--     real states (draft, hidden, cancelled).
--
-- Dormant tables (profiles, event_registrations) are preserved verbatim from
-- v1 so the PWA's Wrapped feature keeps working. They are NOT to be developed
-- further per impeccable.md.
-- ============================================================================


-- ----------------------------------------------------------------------------
-- 1. Extensions
-- ----------------------------------------------------------------------------

-- pg_trgm: trigram similarity for the event-deduplication matcher.
CREATE EXTENSION IF NOT EXISTS "pg_trgm" WITH SCHEMA "extensions";

-- pgcrypto: gen_random_uuid() for the dormant profiles table.
CREATE EXTENSION IF NOT EXISTS "pgcrypto" WITH SCHEMA "extensions";


-- ----------------------------------------------------------------------------
-- 2. Enum types
-- ----------------------------------------------------------------------------

-- Event lifecycle. Replaces v1.events.is_hidden boolean which conflated
-- "hidden by admin" with "cancelled by organizer" — distinct states that
-- need distinct UI treatment ("Event has been cancelled" vs invisible).
CREATE TYPE "public"."event_status" AS ENUM (
    'draft',      -- internal, not yet published
    'published',  -- live in the directory (default)
    'hidden',     -- admin-suppressed (spam, duplicate, low quality)
    'cancelled'   -- organizer cancelled — keep visible with strikethrough
);

-- Whether an event happens in person, online, or both. Derived at ingest
-- from location_type / virtual_info; stored explicitly so filter chips
-- don't have to text-match "Online" prefixes.
CREATE TYPE "public"."event_format" AS ENUM (
    'in_person',
    'online',
    'hybrid'
);

-- How a row entered our system. Drives source_priority defaults: a
-- partnership-tokenized FB scrape outranks an HTML-scraped Luma page for
-- the same event. Manual takes precedence over everything for hand-edits.
CREATE TYPE "public"."ingest_kind" AS ENUM (
    'manual',         -- admin-entered or admin-edited
    'partnership',    -- tokenized API access via formal partnership (FB pages)
    'public_api',     -- documented public API (none today; reserved)
    'public_scrape'   -- HTML / __NEXT_DATA__ extraction (default)
);


-- ----------------------------------------------------------------------------
-- 3. Trigger function — set_updated_at
-- ----------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION "public"."set_updated_at"()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$;


-- ============================================================================
-- 4. ACTIVE TABLES (designed v2)
-- ============================================================================


-- ----------------------------------------------------------------------------
-- organizations — the editorial unit. JSCebu / GOAB / PizzaPy etc.
-- One row per real-world community or solo host, regardless of platforms.
-- This table IS the whitelist (verified_at = trust signal).
-- ----------------------------------------------------------------------------

CREATE TABLE "public"."organizations" (
    "id"                  bigint        NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "slug"                text          NOT NULL,
    "name"                text          NOT NULL,
    "tagline"             text,                       -- one-line description shown on cards
    "description"         text,                       -- markdown ok, longer-form
    "primary_photo"       text,                       -- logo / avatar URL (R2)
    "cover_photo"         text,                       -- banner image URL (R2)

    -- Geographic anchor (PH-wide flexibility per design discussion).
    "city"                text,
    "region"              text,
    "country"             text          DEFAULT 'PH',

    -- Lead-contact metadata. Free-text by design — we explicitly chose
    -- not to model "people" as relational entities (see design notes).
    -- If a partner has multiple leads, list them comma-separated in
    -- lead_contact_name. Upgrade path: split into a persons table later
    -- if/when speaker profiles get prioritized.
    "lead_contact_name"   text,
    "lead_contact_email"  text,
    "lead_contact_role"   text,         -- "Founder", "Lead Organizer"

    -- {twitter, instagram, linkedin, facebook, youtube, tiktok, website}.
    -- jsonb instead of separate columns because the set churns; admin UI
    -- can validate keys against a known list without a schema migration.
    "social_links"        jsonb,
    "founded_year"        int,

    -- Per-org override of the default source priority. NULL = use global
    -- default ['luma', 'facebook', 'meetup', 'eventbrite', 'website'].
    "source_priority"     text[],

    -- Whitelist marker. Set BY HAND when a partnership is established.
    -- The PWA shows a checkmark next to verified orgs. This field is the
    -- single most-load-bearing piece of editorial state in the DB.
    "verified_at"         timestamptz,

    "is_individual"       boolean       NOT NULL DEFAULT false,
    "is_active"           boolean       NOT NULL DEFAULT true,

    "created_at"          timestamptz   NOT NULL DEFAULT now(),
    "updated_at"          timestamptz   NOT NULL DEFAULT now(),

    PRIMARY KEY ("id"),
    UNIQUE ("slug")
);

COMMENT ON TABLE  "public"."organizations" IS 'Editorial whitelist. One row per real-world community / solo host. verified_at = the partnership stamp shown as a checkmark in the PWA.';
COMMENT ON COLUMN "public"."organizations"."verified_at" IS 'Set by hand when a partnership is established. Drives the verified-organizer badge. The PWA aggregates non-verified orgs too, but only verified ones get the trust signal.';
COMMENT ON COLUMN "public"."organizations"."source_priority" IS 'Per-org override of canonical-content priority. NULL = use global default.';
COMMENT ON COLUMN "public"."organizations"."social_links" IS 'jsonb keyed by platform: {twitter, instagram, linkedin, facebook, youtube, tiktok, website}. Admin UI validates keys.';

CREATE TRIGGER "set_organizations_updated_at"
    BEFORE UPDATE ON "public"."organizations"
    FOR EACH ROW EXECUTE FUNCTION "public"."set_updated_at"();


-- ----------------------------------------------------------------------------
-- accounts — per-platform identity. One row per FB page, Luma calendar,
-- Luma user, Meetup group, etc. account_id is the platform's stable ID
-- (text — Luma's "cal-..." / "usr-..." don't fit bigint).
-- ----------------------------------------------------------------------------

CREATE TABLE "public"."accounts" (
    -- Platform-stable text id is the natural PK. v1 had a redundant bigint
    -- "id" that nothing pointed at — dropped here.
    "account_id"        text          NOT NULL,
    "name"              text          NOT NULL,

    -- Source platform: 'facebook' | 'luma' | 'meetup' | 'eventbrite'.
    "type"              text          NOT NULL,

    -- Platform-presence kind. Tells us which scraper code path applies
    -- and what shape account_details holds.
    --   fb_page | luma_calendar | luma_user | meetup_group | eventbrite_organizer
    "kind"              text          NOT NULL,

    "primary_photo"     text,

    -- Promoted from v1.account_details.path. The seed string the cron
    -- functions use to discover this account's events:
    --   'awsugcebu'      — Luma calendar slug
    --   'user/lisksea'   — Luma user handle path
    --   'pizzapy'        — FB page username
    "discovery_path"    text,

    -- Genuinely platform-specific blob — kept narrow.
    -- For FB: { internal_page_id?, locale? }
    -- For Luma: { user_api_id?, calendar_admin_user? }
    "account_details"   jsonb,

    -- How we obtain data from this account. Drives canonical-content
    -- priority — partnership beats public_scrape for the same event.
    "ingest_kind"       "public"."ingest_kind" NOT NULL DEFAULT 'public_scrape',

    -- Editorial grouping: many accounts can belong to one organization.
    -- (PizzaPy's FB page + Luma calendar both link to org "PizzaPy".)
    "organization_id"   bigint
        REFERENCES "public"."organizations"("id")
        ON UPDATE CASCADE ON DELETE SET NULL,

    "is_active"         boolean       NOT NULL DEFAULT true,

    "created_at"        timestamptz   NOT NULL DEFAULT now(),
    "updated_at"        timestamptz   NOT NULL DEFAULT now(),

    PRIMARY KEY ("account_id")
);

COMMENT ON TABLE  "public"."accounts" IS 'Per-platform identity. One row per FB page / Luma calendar / Luma user / Meetup group / etc. account_id is the platform-stable text id.';
COMMENT ON COLUMN "public"."accounts"."discovery_path" IS 'Seed string the cron jobs use to discover events. Promoted from v1 account_details.path for type-safety.';
COMMENT ON COLUMN "public"."accounts"."ingest_kind" IS 'Reliability tier of how we obtain data from this account. Partnership > public_api > public_scrape > manual (where manual takes precedence for hand-edits).';


-- ----------------------------------------------------------------------------
-- account_secrets — sensitive auth material kept apart from the public-
