COMMENT ON TABLE "public"."account_secrets" IS 'Sensitive auth material split from accounts. RLS restricts to service_role. Never expose via PostgREST.';

CREATE TRIGGER "set_accounts_updated_at"
    BEFORE UPDATE ON "public"."accounts"
    FOR EACH ROW EXECUTE FUNCTION "public"."set_updated_at"();

CREATE TRIGGER "set_account_secrets_updated_at"
    BEFORE UPDATE ON "public"."account_secrets"
    FOR EACH ROW EXECUTE FUNCTION "public"."set_updated_at"();


-- ----------------------------------------------------------------------------
-- venues — physical place reusable across events.
-- Optional: events without a venue keep a free-text events.location.
-- An organization can own a venue (enspace, Innovation Hub) so we can
-- show "events at enspace" pages.
-- ----------------------------------------------------------------------------

CREATE TABLE "public"."venues" (
    "id"             bigint        NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "slug"           text          NOT NULL,
    "name"           text          NOT NULL,
    "address_line"   text,
    "city"           text,
    "region"         text,
    "country"        text          DEFAULT 'PH',

    -- jsonb { latitude, longitude } — same shape as v1 events.location_details.
    -- PostGIS would be nicer but adds an extension dependency for marginal
    -- gain at our scale. Upgradeable later.
    "coordinate"     jsonb,

    "capacity"       int,
    "primary_photo"  text,

    -- Org that owns/operates this venue (e.g. enspace owns its venue).
    -- NULL for community venues we don't attribute.
    "parent_org_id"  bigint
        REFERENCES "public"."organizations"("id")
        ON UPDATE CASCADE ON DELETE SET NULL,

    -- Verified venue = admin has confirmed this isn't a typo / merger
    -- candidate. Useful for /venues/[slug] page eligibility.
    "is_verified"    boolean       NOT NULL DEFAULT false,
    "is_active"      boolean       NOT NULL DEFAULT true,

    "created_at"     timestamptz   NOT NULL DEFAULT now(),
    "updated_at"     timestamptz   NOT NULL DEFAULT now(),

    PRIMARY KEY ("id"),
    UNIQUE ("slug")
);

COMMENT ON TABLE "public"."venues" IS 'Physical place reusable across events. NULL on online events. Slug-stable for /venues/[slug] pages.';

CREATE TRIGGER "set_venues_updated_at"
    BEFORE UPDATE ON "public"."venues"
    FOR EACH ROW EXECUTE FUNCTION "public"."set_updated_at"();


-- ----------------------------------------------------------------------------
-- events — canonical event row. Content here is overwritten by the
-- highest-priority source per source_priority rules. Original raw
-- payloads stay in event_source_links.raw.
-- ----------------------------------------------------------------------------

CREATE TABLE "public"."events" (
    "id"                       bigint                  NOT NULL GENERATED BY DEFAULT AS IDENTITY,

    "name"                     text                    NOT NULL,
    "description"              text,

    -- Time, with named timezone preserved separately. start_time stores
    -- the offset Luma/FB emit; timezone (e.g. 'Asia/Manila') is the named
    -- zone for display ("8 PM PHT") and DST-safe future calculations.
    "start_time"               timestamptz             NOT NULL,
    "end_time"                 timestamptz,
    "timezone"                 text,

    -- Free-text location string — fallback for events without a venue
    -- (online, "TBD", "somewhere downtown"). When venue_id is set, the
    -- venue's name/address is canonical.
    "location"                 text,

    -- Coordinates for unmapped venues (geocoded on ingest). Redundant
    -- with venues.coordinate when venue_id is set; keep both because not
    -- every event becomes a venue row.
    "location_details"         jsonb,

    "venue_id"                 bigint
        REFERENCES "public"."venues"("id")
        ON UPDATE CASCADE ON DELETE SET NULL,

    -- Denormalized geographic fields (work for online events too — an
    -- online event "from Cebu City" is a valid editorial classification).
    -- Trigger could sync from venue but easier to write at ingest time.
    "city"                     text,
    "region"                   text,
    "country"                  text                    DEFAULT 'PH',

    -- Image fields. cover_photo is the canonical R2 URL. cover_blurhash
    -- is the 30-char placeholder shown instantly during card render —
    -- improves LCP on card lists by ~300ms in our testing.
    "cover_photo"              text,
    "cover_blurhash"           text,

    "status"                   "public"."event_status" NOT NULL DEFAULT 'published',
    "format"                   "public"."event_format" NOT NULL DEFAULT 'in_person',

    -- Free/paid signal for the "Free" filter chip. NULL = unknown
    -- (don't claim an event is free when we don't know).
    "is_free"                  boolean,
    "price_min"                numeric,
    "price_max"                numeric,
    "currency"                 text                    DEFAULT 'PHP',

    -- Editorial features.
    "is_featured"              boolean                 NOT NULL DEFAULT false,

    -- Slug stays denormalized on events for fast single-row reads. The
    -- full slug history lives in event_slugs (so old URLs keep working
    -- after a rename). events.slug is always == the row in event_slugs
    -- where is_current=true.
    "slug"                     text,

    -- Which event_source_links row holds the source-of-truth content.
    -- Updated by the matcher per the owning org's source_priority.
    "primary_source_link_id"   bigint,

    "created_at"               timestamptz             NOT NULL DEFAULT now(),
    "updated_at"               timestamptz             NOT NULL DEFAULT now(),

    PRIMARY KEY ("id"),
    UNIQUE ("slug")
);

COMMENT ON TABLE  "public"."events" IS 'Canonical event row. Content here is the highest-priority source''s version per the owning org''s source_priority. All raw payloads live in event_source_links.raw.';
COMMENT ON COLUMN "public"."events"."status" IS 'Lifecycle: draft (internal), published (default, live), hidden (admin-suppressed), cancelled (organizer cancelled, keep visible).';
COMMENT ON COLUMN "public"."events"."cover_blurhash" IS '30-char placeholder for instant LCP. Computed at ingest from cover_photo.';
COMMENT ON COLUMN "public"."events"."primary_source_link_id" IS 'Which event_source_links row''s content is currently canonical. Updated by the ingest pipeline based on source_priority.';

CREATE TRIGGER "set_events_updated_at"
    BEFORE UPDATE ON "public"."events"
    FOR EACH ROW EXECUTE FUNCTION "public"."set_updated_at"();


-- ----------------------------------------------------------------------------
-- event_source_links — every place we found this event. Many-to-one to
-- events. Original raw payload preserved here for diff/audit and for
-- re-deriving canonical content if the org switches source priority.
-- ----------------------------------------------------------------------------

CREATE TABLE "public"."event_source_links" (
    "id"            bigint                   NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "event_id"      bigint                   NOT NULL,

    -- Source platform: 'facebook' | 'luma' | 'meetup' | 'eventbrite' | 'website' | 'manual'
    "source"        text                     NOT NULL,
    -- Platform-specific event id (FB graph id, Luma 'evt-...', etc).
    "source_id"     text                     NOT NULL,
    "url"           text,

    -- Reliability tier of this specific scrape (may differ from the
    -- account's default — e.g. a manual override).
    "ingest_kind"   "public"."ingest_kind"   NOT NULL DEFAULT 'public_scrape',

    "scraped_at"    timestamptz              NOT NULL DEFAULT now(),

    -- Full raw payload from the source. Trim periodically (>12mo) to
    -- keep DB size bounded — see runbook.
    "raw"           jsonb,

    "created_at"    timestamptz              NOT NULL DEFAULT now(),
    "updated_at"    timestamptz              NOT NULL DEFAULT now(),

    PRIMARY KEY ("id"),
    UNIQUE ("source", "source_id"),
    FOREIGN KEY ("event_id") REFERENCES "public"."events"("id")
        ON DELETE CASCADE
);

COMMENT ON TABLE "public"."event_source_links" IS 'Many-to-one to events. One row per platform-presence. UNIQUE(source, source_id) ensures each external event becomes at most one link.';

CREATE TRIGGER "set_event_source_links_updated_at"
    BEFORE UPDATE ON "public"."event_source_links"
    FOR EACH ROW EXECUTE FUNCTION "public"."set_updated_at"();

-- Now that event_source_links exists, the FK from events back to it can be added.
ALTER TABLE "public"."events"
    ADD CONSTRAINT "events_primary_source_link_id_fkey"
    FOREIGN KEY ("primary_source_link_id")
    REFERENCES "public"."event_source_links"("id")
    ON DELETE SET NULL;


-- ----------------------------------------------------------------------------
-- event_organizers — many-to-many events ↔ accounts.
-- position=0 is the primary host (kept in scrape order).
-- ----------------------------------------------------------------------------

CREATE TABLE "public"."event_organizers" (
    "event_id"    bigint        NOT NULL,
    "account_id"  text          NOT NULL,
    "role"        text          NOT NULL DEFAULT 'host',
    "position"    int           NOT NULL DEFAULT 0,
    "created_at"  timestamptz   NOT NULL DEFAULT now(),

    PRIMARY KEY ("event_id", "account_id"),
    FOREIGN KEY ("event_id") REFERENCES "public"."events"("id") ON DELETE CASCADE,
    FOREIGN KEY ("account_id") REFERENCES "public"."accounts"("account_id")
        ON UPDATE CASCADE ON DELETE CASCADE
);

COMMENT ON TABLE  "public"."event_organizers" IS 'Many-to-many between events and accounts. position=0 = primary host (the "Presented by" entity).';
COMMENT ON COLUMN "public"."event_organizers"."role" IS 'presenter | cohost | host. Reserved for expansion (sponsor, partner) but speakers explicitly NOT modeled here per impeccable.md.';


-- ----------------------------------------------------------------------------
-- event_slugs — slug history for URL stability.
-- When an event is renamed, the old slug stays here forever pointing at
-- the same event_id. Old URLs always resolve.
-- ----------------------------------------------------------------------------

CREATE TABLE "public"."event_slugs" (
    "slug"         text          NOT NULL,
    "event_id"     bigint        NOT NULL,
    "is_current"   boolean       NOT NULL DEFAULT true,
    "created_at"   timestamptz   NOT NULL DEFAULT now(),

    PRIMARY KEY ("slug"),
    FOREIGN KEY ("event_id") REFERENCES "public"."events"("id")
        ON UPDATE CASCADE ON DELETE CASCADE
);

COMMENT ON TABLE "public"."event_slugs" IS 'Slug history. Old slugs keep pointing at the same event so URLs never 404 after a rename.';

-- A single event can only have one is_current=true slug.
CREATE UNIQUE INDEX "event_slugs_one_current_per_event"
    ON "public"."event_slugs" ("event_id")
    WHERE "is_current" = true;


-- ----------------------------------------------------------------------------
-- tags — editorial taxonomy. Filter chips on the PWA come from here
-- where is_canonical=true. Auto-detected tags (LLM/keyword) sit at
-- is_canonical=false until an admin promotes them.
-- ----------------------------------------------------------------------------

CREATE TABLE "public"."tags" (
    "id"             bigint        NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "slug"           text          NOT NULL,
    "name"           text          NOT NULL,

    -- topic | format | level | language | other
    --   topic    — ai, blockchain, web3, design, devops, mobile, security, data
    --   format   — talk, workshop, hackathon, conference, meetup, social
    --   level    — beginner, intermediate, advanced, all_levels
    --   language — english, filipino, cebuano
    --   other    — free, hiring, networking
    "kind"           text          NOT NULL,

    -- true = appears as a filter chip / on org pages.
    -- false = scraper-suggested, awaiting admin review.
    "is_canonical"   boolean       NOT NULL DEFAULT false,

    "description"    text,         -- admin guidance: "Apply when ≥30% of talks are AI-focused"
    "color"          text,         -- optional hex for chip color override

    "created_at"     timestamptz   NOT NULL DEFAULT now(),
    "updated_at"     timestamptz   NOT NULL DEFAULT now(),

    PRIMARY KEY ("id"),
    UNIQUE ("slug")
);

COMMENT ON TABLE  "public"."tags" IS 'Editorial taxonomy. Filter chips come from is_canonical=true. Scraper-suggested tags wait at is_canonical=false until promoted.';
COMMENT ON COLUMN "public"."tags"."kind" IS 'topic | format | level | language | other. Drives chip groupings in the PWA filter UI.';

CREATE TRIGGER "set_tags_updated_at"
    BEFORE UPDATE ON "public"."tags"
    FOR EACH ROW EXECUTE FUNCTION "public"."set_updated_at"();


-- ----------------------------------------------------------------------------
-- event_tags — many-to-many between events and tags. source distinguishes
-- editorial decisions from auto-suggestions so we can re-run extraction
-- without losing curation.
-- ----------------------------------------------------------------------------

CREATE TABLE "public"."event_tags" (
    "event_id"    bigint        NOT NULL,
    "tag_id"      bigint        NOT NULL,

    -- 'manual'  — admin set this tag
    -- 'auto'    — keyword/LLM extraction (re-runnable)
    -- 'scraper' — copied from source platform's own tags (rare)
    "source"      text          NOT NULL DEFAULT 'manual',

    -- For auto-tags: 0..1 confidence. Lets the admin filter by quality.
    "confidence"  real,

    "created_at"  timestamptz   NOT NULL DEFAULT now(),

    PRIMARY KEY ("event_id", "tag_id"),
    FOREIGN KEY ("event_id") REFERENCES "public"."events"("id") ON DELETE CASCADE,
    FOREIGN KEY ("tag_id")   REFERENCES "public"."tags"("id")   ON DELETE CASCADE
);

COMMENT ON TABLE  "public"."event_tags" IS 'M:N between events and tags. source=manual is editorial; source=auto is re-derivable.';


