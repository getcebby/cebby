-- find_event_matches — convenience RPC the ingest pipeline calls when
-- considering whether an incoming scrape should attach to an existing
-- event or create a new one. Trigram similarity + date window.
CREATE OR REPLACE FUNCTION "public"."find_event_matches"(
    p_name text,
    p_start_time timestamptz,
    p_threshold real DEFAULT 0.7,
    p_window_days int DEFAULT 1
)
RETURNS TABLE(id bigint, name text, score real)
LANGUAGE sql
STABLE
AS $$
    SELECT
        e.id,
        e.name,
        extensions.similarity(e.name, p_name) AS score
    FROM public.events e
    WHERE e.start_time BETWEEN p_start_time - (p_window_days || ' days')::interval
                           AND p_start_time + (p_window_days || ' days')::interval
      AND e.name IS NOT NULL
      AND e.status != 'hidden'
      AND extensions.similarity(e.name, p_name) >= p_threshold
    ORDER BY score DESC
    LIMIT 5;
$$;

COMMENT ON FUNCTION "public"."find_event_matches" IS 'Trigram-similarity match for ingest deduplication. Excludes hidden events so a manually-hidden duplicate doesn''t become a re-attach target.';


